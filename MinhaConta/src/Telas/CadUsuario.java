    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        ConectaBanco();
        try {
            Statement stmt = null;
            try {
                stmt = CONN.createStatement();
            } catch (SQLException ex) {
                Logger.getLogger(CadCarteira.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                // pedir o codigo para pesquisar
                c_codigo_pesquisa = JOptionPane.showInputDialog(null, "Digite o código que deseja pesquisar!");

                // comando de select
                ResultSet rs = stmt.executeQuery("SELECT * FROM mauricio_zaquia_usuario WHERE idmauricio_zaquia_usuario = " + c_codigo_pesquisa + ";");
                try {
                    while (rs.next()) {
                        if (rs != null) {
                            txtCodigo.setText(rs.getString("idmauricio_zaquia_usuario"));
                            txtNome.setText(rs.getString("nome"));
                            txtTelefone.setText(rs.getString("telefone"));
                            txtCelular.setText(rs.getString("celular"));
                            txtIdade.setText(rs.getString("idade"));
                            txtEndereco.setText(rs.getString("endereco"));
                            txtCnpjCpf.setText(rs.getString("cnpj_cpf"));

                            String sexo = rs.getString("sexo");

                            if ("M".equals(sexo)) {
                                rbMasculino.setSelected(true);
                            } else {
                                rbFeminino.setSelected(true);
                            }

                            String tfj = rs.getString("tipo_fisica_juridica");

                            if ("F".equals(tfj)) {
                                rbFisica.setSelected(true);
                            } else {
                                rbJuridica.setSelected(true);
                            }
                        } else {
                            JOptionPane.showMessageDialog(null, "Registro nao encontrado!");
                        }
                    }
                } finally {
                    try {
                        rs.close();
                    } catch (Throwable ignore) {
                        /* Propagate the original exception
                         instead of this one that you may want just logged */
                    }
                    if (txtCodigo.getText().length() == 0) {
                        JOptionPane.showMessageDialog(null, "Registro nao encontrado!");
                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(CadCarteira.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                try {
                    stmt.close();
                } catch (Throwable ignore) {
                }
            }
        } finally {
            try {
                CONN.close();
            } catch (Throwable ignore) {
            }
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

