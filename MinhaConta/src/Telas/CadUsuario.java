    private void bloqueios() {
        iBloqueios = 0;

        if (txtNome.getText().length() == 0) {
            iBloqueios = 1;
        };
        if (txtEndereco.getText().length() == 0) {
            iBloqueios = 2;
        };
        if (txtIdade.getText().length() == 0) {
            iBloqueios = 3;
        };
        if (txtCnpjCpf.getText().length() == 0) {
            iBloqueios = 4;
        }

        if (iBloqueios == 1) {
            JOptionPane.showMessageDialog(null, "Nome não pode ficar em branco!");
            throw new IllegalArgumentException("Nome não pode ficar em branco!");
        }
        if (iBloqueios == 2) {
            JOptionPane.showMessageDialog(null, "Endereço não pode ficar em branco!");
            throw new IllegalArgumentException("Endereço não pode ficar em branco!");
        }
        if (iBloqueios == 3) {
            JOptionPane.showMessageDialog(null, "Idade não pode ficar em branco!");
            throw new IllegalArgumentException("Idade não pode ficar em branco!");
        }
        if (iBloqueios == 4) {
            JOptionPane.showMessageDialog(null, "Cnpj/cpf não pode ficar em branco!");
            throw new IllegalArgumentException("Cnpj/cpf não pode ficar em branco!");
        }
    }

