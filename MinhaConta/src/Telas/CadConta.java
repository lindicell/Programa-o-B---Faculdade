    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String linha_banco = "";
        String caminho_salvar = "";
        try {
            Classes.ClassConta x1 = new ClassConta();
            ClasseGenericaGravar g = new ClasseGenericaGravar();

            x1.setIdconta(Integer.parseInt(txtCodigo.getText()));
            x1.setDescricao(txtDescricao.getText());
            x1.setData(txtDataCadastro.getText());
            x1.setValor(Float.parseFloat(txtValor.getText()));
            x1.setFktipo_conta(cbTipo.getSelectedIndex());
            //x1.setFktipo_conta(cbTipo.getSelectedItem().toString());

            linha_banco = linha_banco + " idconta " + x1.getIdconta() + ';';
            linha_banco = linha_banco + " descricao " + x1.getDescricao() + ';';
            linha_banco = linha_banco + " tipo_conta " + x1.getFktipo_conta() + ';';
            linha_banco = linha_banco + " valor " + x1.getValor() + ';';
            linha_banco = linha_banco + " data_cadastro " + x1.getData() + ';';

            Genericas.CaminhoGravarLer cgl = new Genericas.CaminhoGravarLer();            
            caminho_salvar = JOptionPane.showInputDialog("Digite o caminho para gravar junto com o nome", cgl.CAMINHOGRAVARLER);

            if (caminho_salvar != null) {
                g.GravarArquivo(caminho_salvar, linha_banco);
            }

        } catch (NumberFormatException E) {
            System.out.println("Telas.CadConta.btnCadastrarActionPerformed()" + E.getMessage() + E.getClass());
            JOptionPane.showMessageDialog(null, "Houve um erro na hora de cadastrar a conta, verifique os campos numéricos e tente novamente!");
        } catch (Exception E) {
            System.out.println("Telas.CadConta.btnCadastrarActionPerformed()" + E.getMessage() + E.getClass());
            JOptionPane.showMessageDialog(null, "Houve um erro na hora de cadastrar a conta, tente novamente!");
        } finally {
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

